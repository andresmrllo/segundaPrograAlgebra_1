    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            for (JTextField f : lista) {
                if (f.getText().length() == 0) {
                    JOptionPane.showMessageDialog(null, "Debe completar todos los datos");
                    f.requestFocus();
                    return;
                }
            }
            int contadorFilas = 1;
            int contadorColumnas = 1;
            double [][] normal = new double[orden][orden];
            while (contadorFilas <= orden) {
                if (contadorColumnas <= orden) {
                    for (JTextField f : lista) {
                        if (f.getName().equals("Entrada" + contadorFilas + contadorColumnas)) {
                            normal[contadorFilas - 1][contadorColumnas - 1] = Float.parseFloat(f.getText());
                            contadorColumnas++;
                            break;
                        }
                    }
                } else {
                    contadorFilas++;
                    contadorColumnas = 1;
                }
            }
            System.out.println("Original:"+Arrays.deepToString(normal));
            RealMatrix matriz = MatrixUtils.createRealMatrix(normal);
            RealMatrix L = new LUDecomposition(matriz).getL();
            RealMatrix U = new LUDecomposition(matriz).getU();
            if (L != null && U != null){
                MatricesLURespuesta respuesta = new MatricesLURespuesta(U,L);
                respuesta.setVisible(true);
                this.dispose();
                System.out.println(L);
                System.out.println(U);
            }
            else{
                JOptionPane.showMessageDialog(null, "La matriz es singular por lo tanto no se puede factorizar");
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error");
        }
        
        
        
        
        
        
       
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

